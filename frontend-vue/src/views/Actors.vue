<template>
  <div id="main">
    <div class="header">
      <h1 class="title">Actors</h1>
    </div>

    <div class="content">
      <table id="actors">
        <thead>
          <tr>
            <th>#</th>
            <th>Name</th>
            <th>Date of Birth</th>
          </tr>
        </thead>
        <tbody>
          <tr v-for="actor in actors" :key="actor.id">
            <td>{{actor.id}}</td>
            <td>{{actor.firstName}}</td>
            <td>{{actor.dateOfBirth}}</td>
          </tr>
        </tbody>
      </table>
    </div>
    {{actors}}
  </div>
